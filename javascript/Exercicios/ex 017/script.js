i = 1;

// while
console.log('Estrutura While\n')
while (i <= 10) {
    console.log(`Número ${i}`);
    i++;
}

// do while
j = 1;
console.log('\nEstrutura Do-While\n')
do {
    console.log(`Número ${j}`);
    j++;
} while (j <= 10)

// for
console.log('\nEstrutura For\n')
for (k = 1; k <= 10; k++) {
    console.log(`Número ${k}`)
}